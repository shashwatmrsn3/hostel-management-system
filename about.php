 <p> Deurali Hostel offers comfy, friendly accommodation at great prices in Kathmandu. Here you will find a multicultural atmosphere and you will receive value for money. With its city centre location, you will find it easy to get to all the citys sites and attractions, most of these which are reachable by foot. Durbar square, new road etc are just a few minutes walk away.

All the rooms at this hostel are single  rooms. There is a spearete block for boys and girls hostel. There are rooms accommodating 4, 5, 6 and 8 students and dorms for 18 students. Dorms are sectioned off to create a more private environment. All prices include taxes. There are 96 beds in the hostel and compared to the bigger Shelter City, the sister hostel, Deurali Hsotel is situated in a quieter area of Kathmandu.

The hostel provides great facilities and a welcoming atmosphere to make your stay more enjoyable. The staff is international, they all speak English and are always happy to help. If you do not have a padlock for the lockers, you can purchase one from reception.

There is a terrace in the garden and a cafe in the hostel. The cafe offers food and drink and is a nice spot to relax and meet fellow students who come from all parts of the world. Smoking, drugs or alcohol are not allowed in the hostel but cigarettes can be smoked in the garden.
